package org.obrii.fit.mit.BaidaSpring.Domain;

/**
 *
 * @author danila
 */
public class Entity {
    
    private  long id;
    private  String name;
    private String email;

    public Entity() {
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}